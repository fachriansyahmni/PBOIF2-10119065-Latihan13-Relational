public class PBOIF2_10119065_Latihan13_Relational {
    public static void main(String[] args) {
        int bil1 = 10;
        int bil2 = 20;
        System.out.println("a == b = " + (bil1 == bil2));
        System.out.println("a != b = " + (bil1 != bil2));
        System.out.println("a > b = " + (bil1 > bil2));
        System.out.println("a < b = " + (bil1 < bil2));
        System.out.println("b >= a = " + (bil2 >= bil1));
        System.out.println("b <= a = " + (bil2 <= bil1));
    }
    
}
